export const DATE_FORMAT = "DD-MM-YYYY"

// Date Formats:
//      DD-MM-YYYY: 17-09-2025 (Day-Month-Year with leading zeros)
//      MM-DD-YYYY: 09-17-2025 (Month-Day-Year with leading zeros)
//      YYYY-MM-DD: 2025-09-17 (Year-Month-Day, commonly used in ISO 8601)
//      DD/MM/YYYY: 17/09/2025 (Day/Month/Year with leading zeros)
//      MM/DD/YYYY: 09/17/2025 (Month/Day/Year with leading zeros)
//      DD MMMM YYYY: 17 September 2025 (Day, full month name, year)
//      MMMM DD, YYYY: September 17, 2025 (Full month name, day, year)
// Date and Time Formats:
//      DD-MM-YYYY HH:mm:ss: 17-09-2025 20:25:00 (Date and time with 24-hour format and seconds)
//      YYYY-MM-DDTHH:mm:ssZ: 2025-09-17T20:25:00Z (ISO 8601 format, 'T' separator, 'Z' for UTC)
//      YYYY-MM-DD HH:mm:ss.SSS: 2025-09-17 20:25:00.123 (Date and time with milliseconds)
//      ddd, DD MMM YYYY HH:mm:ss GMT: Wed, 17 Sep 2025 20:25:00 GMT (RFC 1123 format)